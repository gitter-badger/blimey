namespace Blimey.Platform
{
    using System;
    using System.Collections.Generic;
    using System.Text;

    // ────────────────────────────────────────────────────────────────────── //

    public static class ListExtensions
    {
        public static TP AddEx<TC, TP> (this IList <TC> me, TP item) where TP : TC
        {
            me.Add (item);
            return item;
        }

        public static String Join<T> (this IEnumerable<T> me, String seperator)
        {
            var sb = new StringBuilder ();
            foreach (var value in me)
            {
                if (sb.Length > 0)
                    sb.Append (seperator);
                sb.Append (value);
            }
            return sb.ToString ();
        }

        public static void Shuffle<T>(this IList<T> list)
        {
            Random rng = new Random ();
            int n = list.Count;
            while (n > 1)
            {
                n--;
                int k = rng.Next(n + 1);
                T value = list[k];
                list[k] = list[n];
                list[n] = value;
            }
        }
    }
}
