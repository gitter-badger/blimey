namespace Blimey
{
    using System;
    using Abacus.SinglePrecision;
    using Fudge;

    // ────────────────────────────────────────────────────────────────────── //

    public static class Rgba32Extensions
    {
        public static Vector4 ToVector4 (this Rgba32 value)
        {
            return new Vector4 (
                (Single)value.R / 255f,
                (Single)value.G / 255f,
                (Single)value.B / 255f,
                (Single)value.A / 255f);
        }
    }
}